public class Lexeme {
    private String token;
    private int code;
    //private int ascii;
    
    public Lexeme(String t, int c){
        this.token = t;
        this.code = c;       
    }
    public Lexeme(){
        
    }

    /**
     * @return the token
     */
    public String getToken() {
        return token;
    }

    /**
     * @param token the token to set
     */
    public void setToken(String token) {
        this.token = token;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }

}
